const initialState = {
  currentUser: null,
};

export const SET_CURRENT_USER = "SET_CURRENT_USER";

const userReducer = (state = initialState, action) => {
  switch (action.type) {
    case SET_CURRENT_USER:
      return {
        ...state,
        currentUser: action.payload,
      };

    default:
      return state;
  }
};

export default userReducer;
